numero = int(input("Escriba un número: "))
if (numero == 0): 
  print("El numero",numero, " es cero")
elif (numero > 0): 
  print("El numero",numero, "es positivo ")
elif (numero < 0): 
  print("El numero",numero, "es negativo")

      
    
